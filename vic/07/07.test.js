import { rotateArray } from "./rotateArray.js";

describe("測試能不能成功旋轉", () => {
  test("這個按照順序1到9的矩陣會向右旋轉90度", () => {
    const matrix = [
      [1, 2, 3],
      [4, 5, 6],
      [7, 8, 9],
    ];

    const result = rotateArray(matrix);
    const expected = [
      [7, 4, 1],
      [8, 5, 2],
      [9, 6, 3],
    ];
    expect(result).toEqual(expected);
  });
  test("會把這個圖形向右旋轉90度", () => {
    const matrix = [
      [" ", "*", " "],
      ["*", "*", "*"],
      [" ", " ", " "],
    ];

    const result = rotateArray(matrix);
    const expected = [
      [" ", "*", " "],
      [" ", "*", "*"],
      [" ", "*", " "],
    ];
    expect(result).toEqual(expected);
  });
  test("測試愛心旋轉90度", () => {
    const matrix = [
      [" ", " ", "*", "*", " ", "*", "*", " ", " "],
      [" ", "*", "*", "*", "*", "*", "*", "*", " "],
      ["*", "*", "*", "*", "*", "*", "*", "*", "*"],
      ["*", "*", "*", "*", "*", "*", "*", "*", "*"],
      [" ", "*", "*", "*", "*", "*", "*", "*", " "],
      [" ", " ", "*", "*", "*", "*", "*", " ", " "],
      [" ", " ", " ", "*", "*", "*", " ", " ", " "],
      [" ", " ", " ", " ", "*", " ", " ", " ", " "],
    ];

    const result = rotateArray(matrix);
    const expected = [
      [" ", " ", " ", " ", "*", "*", " ", " "],
      [" ", " ", " ", "*", "*", "*", "*", " "],
      [" ", " ", "*", "*", "*", "*", "*", "*"],
      [" ", "*", "*", "*", "*", "*", "*", "*"],
      ["*", "*", "*", "*", "*", "*", "*", " "],
      [" ", "*", "*", "*", "*", "*", "*", "*"],
      [" ", " ", "*", "*", "*", "*", "*", "*"],
      [" ", " ", " ", "*", "*", "*", "*", " "],
      [" ", " ", " ", " ", "*", "*", " ", " "],
    ];

    expect(result).toEqual(expected);
  });
});
